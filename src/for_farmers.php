            <h3>1. Agroecológicos x Orgânicos</h3>
          </div>
          <p>
          Inicialmente, para ser considerado produto agroecológico ou orgânico é necessária uma certificação de instituições
          credenciadas pelo Ministério da Agricultura, Pecuária e Abastecimento que atuam no Sistema Brasileiro de Avaliação
          da Conformidade Orgânica. Para além disso, enquanto o cultivo orgânico está diretamente relacionado com a eliminação
          do uso de agrotóxicos, a agroecologia está relacionada à valorização da diversidade dos produtos,
          da cultura local e do bem estar do produtor agrícola.
          </p>
          <p>
          Na produção do produto orgânico os insumos são por compostagens, deve haver assistência técnica e a mão de obra deve ser manual.
          No entanto, não existe restrição para uma monocultura de orgânicos, enquanto a agroecologia prega a pluricultura com
          rotação frequente.  
          </p>
